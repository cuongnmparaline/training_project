    // tinh phu cap / AJAX
    $('#tinhPhuCap').click(function(){

        var idNhanVien = $('#idNhanVien').val();
        var soNgayCong = $('#soNgayCong').val();
        if(soNgayCong != '')
        {
            $.ajax({
                url: "?controller=salary&action=calculateAllowance",
                method: "POST",
                data:{idNhanVien:idNhanVien,
                    soNgayCong:soNgayCong
                },
                dataType: "JSON",
                success:function(data)
                {
                    document.getElementById('phuCap').value = data;
                },
                error: function (jqXHR, exception) {
                    var msg = '';
                    if (jqXHR.status === 0) {
                        msg = 'Not connect.\n Verify Network.';
                    } else if (jqXHR.status == 404) {
                        msg = 'Requested page not found. [404]';
                    } else if (jqXHR.status == 500) {
                        msg = 'Internal Server Error [500].';
                    } else if (exception === 'parsererror') {
                        msg = 'Requested JSON parse failed.';
                    } else if (exception === 'timeout') {
                        msg = 'Time out error.';
                    } else if (exception === 'abort') {
                        msg = 'Ajax request aborted.';
                    } else {
                        msg = 'Uncaught Error.\n' + jqXHR.responseText;
                    }
                    console.log(msg);
                },
            })
        }
        else
        {
            alert("Lỗi! Vui lòng nhập số ngày công.");
        }
    })

